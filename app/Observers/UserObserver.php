    /**
     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        // Set the default role to user unless otherwise specified
        if (!$user->role) {
            $role = Role::where('name', 'user')->firstOrFail();
            $user->role()->associate($role);
            $user->save();
        }
    }

